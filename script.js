// App state
let currentQuestion = 0;
let answers = {};

// DOM elements
const introScreen = document.getElementById('intro-screen');
const testScreen = document.getElementById('test-screen');
const resultsScreen = document.getElementById('results-screen');
const startBtn = document.getElementById('start-btn');
const restartBtn = document.getElementById('restart-btn');
const progressFill = document.getElementById('progress-fill');
const progressText = document.getElementById('progress-text');
const questionBlockTitle = document.getElementById('question-block-title');
const questionText = document.getElementById('question-text');
const scaleOptions = document.querySelectorAll('.scale-option');
const resultsSummary = document.getElementById('results-summary');
const resultsDetails = document.getElementById('results-details');

// Initialize
function init() {
    startBtn.addEventListener('click', startTest);
    restartBtn.addEventListener('click', restartTest);
    
    scaleOptions.forEach(option => {
        option.addEventListener('click', selectAnswer);
    });
}

function startTest() {
    introScreen.classList.remove('active');
    testScreen.classList.add('active');
    showQuestion();
}

function showQuestion() {
    const question = questions[currentQuestion];
    const elementInfo = elementsInfo[question.block];
    
    // Update progress
    const progress = ((currentQuestion) / questions.length) * 100;
    progressFill.style.width = `${progress}%`;
    progressText.textContent = `Вопрос ${currentQuestion + 1} из ${questions.length}`;
    
    // Update question
    questionBlockTitle.textContent = `${elementInfo.icon} ${elementInfo.name}`;
    questionBlockTitle.className = `block-title ${elementInfo.color}`;
    questionText.textContent = question.text;
    
    // Reset scale options
    scaleOptions.forEach(option => {
        option.classList.remove('selected');
    });
    nextBtn.disabled = true;
}

function selectAnswer(e) {
    const value = parseInt(e.target.dataset.value);

    scaleOptions.forEach(option => {
        option.classList.remove('selected');
    });

    e.target.classList.add('selected');

    // Store answer
    const question = questions[currentQuestion];
    if (!answers[question.block]) {
        answers[question.block] = 0;
    }
    answers[question.block] += value;

    // Auto-advance to next question
    setTimeout(() => {
        currentQuestion++;

        if (currentQuestion >= questions.length) {
            showResults();
        } else {
            showQuestion();
        }
    }, 200);
}

function calculateResults() {
    const scores = {};
    const blocks = ['earth', 'water', 'fire', 'air', 'ether'];
    
    blocks.forEach(block => {
        scores[block] = answers[block] || 0;
    });
    
    return scores;
}

function getStatus(score) {
    if (score <= 12) return 'deficit';
    if (score <= 18) return 'norm';
    return 'excess';
}

function getStatusText(status) {
    const texts = {
        deficit: 'Дефицит',
        norm: 'Норма',
        excess: 'Избыток'
    };
    return texts[status];
}

function showResults() {
    testScreen.classList.remove('active');
    resultsScreen.classList.add('active');
    
    const scores = calculateResults();
    
    // Build summary
    let summaryHTML = '';
    Object.keys(scores).forEach(block => {
        const score = scores[block];
        const info = elementsInfo[block];
        const status = getStatus(score);
        
        summaryHTML += `
            <div class="result-item">
                <div class="result-name">
                    <span class="result-icon">${info.icon}</span>
                    <span>${info.name}</span>
                </div>
                <div class="result-value">
                    <span class="result-score ${info.color}">${score}</span>
                    <span class="result-status ${status}">${getStatusText(status)}</span>
                </div>
            </div>
        `;
    });
    resultsSummary.innerHTML = summaryHTML;
    
    // Build details
    let detailsHTML = '';
    Object.keys(scores).forEach(block => {
        const score = scores[block];
        const info = elementsInfo[block];
        const status = getStatus(score);
        const interpretation = interpretations[block][status];
        
        detailsHTML += `
            <div class="detail-block">
                <h3 class="${info.color}">
                    <span class="icon">${info.icon}</span>
                    <span>${info.name}: ${interpretation.title}</span>
                </h3>
                <div class="detail-content">
                    <p><strong>Что это значит:</strong> ${interpretation.meaning}</p>
                    <h4>Как это проявляется в бизнесе:</h4>
                    <ul>
                        ${interpretation.business.map(item => `<li>${item}</li>`).join('')}
                    </ul>
                    <h4>Что делать:</h4>
                    <p>${interpretation.recommendations}</p>
                </div>
            </div>
        `;
    });
    resultsDetails.innerHTML = detailsHTML;
}

function restartTest() {
    currentQuestion = 0;
    answers = {};
    resultsScreen.classList.remove('active');
    introScreen.classList.add('active');
}

// Start the app
init();
